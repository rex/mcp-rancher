#!/usr/bin/env -S uv run --script
# /// script
# requires-python = ">=3.11"
# ///
"""sync_readme_badges.py — keep the README tool counts in lockstep with the manifest.

The tool count lives in exactly ONE authoritative place — `docs/tool-manifest.json`
(generated by `make tool-manifest`, drift-gated in `make validate`) — and flows
from there into the README's tool-count badge and the tool-surface breakdown line.
No human ever hand-edits the number, so it can never go stale.

Modes:
  (default)  rewrite README.md in place — the pre-commit fixer path.
  --check    exit 1 if the README is out of sync (CI / `make validate` guard),
             printing what would change; makes no edits.

Exit codes: 0 in sync (or fixed); 1 out of sync (--check only); 2 bad inputs.
"""

from __future__ import annotations

import argparse
import json
import re
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
README = ROOT / "README.md"
MANIFEST = ROOT / "docs" / "tool-manifest.json"

_KEYS = ("tool_count", "read_only_count", "write_count", "destructive_count")


def _counts() -> tuple[int, int, int, int]:
    data = json.loads(MANIFEST.read_text(encoding="utf-8"))
    missing = [k for k in _KEYS if k not in data]
    if missing:
        raise SystemExit(
            f"tool-manifest.json missing count keys: {missing} — run `make tool-manifest`."
        )
    return tuple(int(data[k]) for k in _KEYS)  # type: ignore[return-value]


def _apply(text: str, counts: tuple[int, int, int, int]) -> str:
    total, read_only, write, destructive = counts
    # shields.io badge:  .../badge/tools-<N>-<color>  and its  alt="<N> tools"
    text = re.sub(r"(/badge/tools-)\d+(-)", rf"\g<1>{total}\g<2>", text)
    text = re.sub(r'alt="\d+ tools"', f'alt="{total} tools"', text)
    # tool-surface breakdown:  **<N> tools: <r> read-only · <w> writes · <d> destructive**
    text = re.sub(
        r"\*\*\d+ tools: \d+ read-only · \d+ writes · \d+ destructive\*\*",
        f"**{total} tools: {read_only} read-only · {write} writes · {destructive} destructive**",
        text,
    )
    return text


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--check", action="store_true", help="verify only; exit 1 if stale")
    args = parser.parse_args()

    original = README.read_text(encoding="utf-8")
    updated = _apply(original, _counts())

    if updated == original:
        return 0
    if args.check:
        print(
            "✗ README tool counts are out of sync with docs/tool-manifest.json — "
            "run `make sync-readme-badges`.",
            file=sys.stderr,
        )
        return 1
    README.write_text(updated, encoding="utf-8")
    print(f"✓ synced README tool counts from the manifest ({_counts()[0]} tools)")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
